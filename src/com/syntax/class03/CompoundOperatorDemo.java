package com.syntax.class03;

public class CompoundOperatorDemo {

	public static void main(String[] args) {
		    int a = 50;
		    double cakePiece = 11;
		    a += 100;
		    cakePiece %= 4;
		    System.out.println(a);
		    System.out.println("--------------------------------------");
		    System.out.println(a);
		    System.out.println("--------------------------------------");
		    System.out.println(cakePiece);
		    System.out.println("--------------------------------------");
		    cakePiece = 25;
		    cakePiece /= 7;
		    System.out.println(cakePiece);
		}
	}


