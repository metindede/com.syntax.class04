package com.syntax.class02;

public class ArithmeticOperatorsDemo {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 5;

		
		System.out.println("Ahmet_" + 10 + 5);
		System.out.println("ahmet_" + 10 + 30);
		System.out.println("ahmet_" + (10 + 30));
		System.out.println("ahmet_" + 10 * 30);
		
		int sum = num1 + num2;

		int sub = num1 - num2;

		int div = num1 / num2;

		int mult = num1 * num2;

		System.out.println("additaion->" + sum);

		System.out.println("subtraction->" + sub);

		System.out.println("division->" + div);

		System.out.println("muttiplication->" + mult);

		double d1 = 3.4;
		double d2 = 5.73;
		double dsum = d1 + d2;

		System.out.println("double additaion->" + dsum);

		double dsum3 = num1 + num2;
		double dsum2 = num1 + num2;

		System.out.println("dsum2->" + dsum2);

	}

}
