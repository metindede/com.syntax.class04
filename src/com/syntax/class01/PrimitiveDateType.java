package com.syntax.class01;

public class PrimitiveDateType {
	
	public static void main (String [] args ) {
		
		
		// whole Number data Types
		byte num =10;
		short num1 =100;
		int num2 =100000;
		long num3 =10453443463L;
		
		//decimal Number Data Types
		
		float num4 = 12.9F;
		
		double num5 = 0.888;
		
		//non- Number Data Types
		
		char add = '+';
		char letter = 'a';
	
		// true or false Data Types (Yes or No)
		// do not put Yes or No for value
		
		boolean hungry = true;
		
		boolean thirsty = false;
		
		
	}

}
